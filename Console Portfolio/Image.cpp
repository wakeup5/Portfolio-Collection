char* Image::get(IMAGE_CG cg)
{
	char* result;
	switch (cg)
	{
	case CG_OPENING:
		result = getOpening();
		break;
	case CG_YEUN_NORMAL:
		result = getYeunNormal();
		break;
	case CG_WONJANG_NORMAL:
		result = getWonjangNormal();
		break;
	case CG_LINA_NORMAL:
		result = getLinaNormal();
		break;
	default:
		result = " ";
		break;
	}

	return result;
}

